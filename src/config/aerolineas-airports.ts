export function getAerolineasAirportInfo(code: string) {
  return aerolineasAirports[code.toUpperCase() as keyof typeof aerolineasAirports];
}

export function isPopularAerolineasRoute(origin: string, destination: string): boolean {
  const route = `${origin.toUpperCase()}-${destination.toUpperCase()}`;
  const reverseRoute = `${destination.toUpperCase()}-${origin.toUpperCase()}`;
  
  return aerolineasRoutes.domestic.includes(route) || 
         aerolineasRoutes.domestic.includes(reverseRoute) ||
         aerolineasRoutes.international.includes(route) ||
         aerolineasRoutes.international.includes(reverseRoute);
}

